
public class student {
	int rollno;
	static String subject=" physic";
	int marks;
	
student(int r,int m){
	rollno=r;
	marks=m;
}
void display(){
	System.out.println(rollno+" "+marks+" "+subject);
}
public static void main(String[] args) {
	student s1=new student(111,70);
	student s2=new student(222,86);
	s1.display();
	s2.display();
}
}
